using System;
namespace ConsoleApp2
{
    class Program
    {
        static void Main(string[] args)
        {
            int s = int.Parse(Console.ReadLine());
            if (s >= 90)
            {
                Console.WriteLine("A");
            }
            else if (s >= 80)
            {
                Console.WriteLine("B");
            }
            else if(s >=70)
            {
                Console.WriteLine("C");
            }
            else if(s >= 60)
            {
                Console.WriteLine("D");
            }
            else
            {
                Console.WriteLine("F");
            }
        }
    }
}
